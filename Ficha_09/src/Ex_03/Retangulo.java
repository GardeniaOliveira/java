package Ex_03;

public class Retangulo {

    private double Area;
    private double Perimetro;

    public Retangulo(double area, double perimetro) {
        Area = area;
        Perimetro = perimetro;
    }

    public double getArea() {
        return Area;
    }

    public double getPerimetro() {
        return Perimetro;
    }
}
