public class Ex_05 {
    public static void main(String[] args) {
        for (int j = 5; j >= 1; j--) {
            for (int l = j; l < 5; l++) {

                System.out.print(j);
            }

            System.out.println(j);
        }

        for (int j = 1; j <= 5; j++) {
            for (int l = j; l < 5; l++) {

                System.out.print(j);
            }

            System.out.println(j);
        }


    }
}
